/**
 * The class IntPair defines a pair of integers used to determine positions on the board.
 */
public class IntPair {
    int x;
    int y;
    public IntPair(int y, int x){
        this.x = x;
        this.y = y;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
}